#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int i;
	char *temp;

	i = 0;
	temp = dest;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (temp);
}
